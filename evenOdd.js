const num = 7;

if (num % 2 === 0) {
    console.log(`${num} is Even`);
} else {
    console.log(`${num} is Odd`);
}

const arr = [1, 2, 3, 4, 5];

for (let i = 0; i < arr.length; i++) {
    if (arr[i] % 2 === 0) {
        console.log(`${arr[i]} is Even`);
    } else {
        console.log(`${arr[i]} is Odd`);
    }
}